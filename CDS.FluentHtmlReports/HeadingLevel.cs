namespace CDS.FluentHtmlReports;

/// <summary>
/// Specifies an HTML heading level (&lt;h1&gt; through &lt;h6&gt;).
/// </summary>
public enum HeadingLevel
{
    /// <summary>Heading level 1 (&lt;h1&gt;), the same format as the report title.</summary>
    H1 = 1,

    /// <summary>Heading level 2 (&lt;h2&gt;).</summary>
    H2 = 2,

    /// <summary>Heading level 3 (&lt;h3&gt;).</summary>
    H3 = 3,

    /// <summary>Heading level 4 (&lt;h4&gt;).</summary>
    H4 = 4,

    /// <summary>Heading level 5 (&lt;h5&gt;).</summary>
    H5 = 5,

    /// <summary>Heading level 6 (&lt;h6&gt;).</summary>
    H6 = 6
}
