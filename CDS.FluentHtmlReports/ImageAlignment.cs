namespace CDS.FluentHtmlReports;

/// <summary>
/// Specifies the horizontal alignment of an image within the report container.
/// </summary>
public enum ImageAlignment
{
    /// <summary>Align the image to the left edge.</summary>
    Left,

    /// <summary>Center the image horizontally (default).</summary>
    Center,

    /// <summary>Align the image to the right edge.</summary>
    Right
}
